i = 0
while i < 10:
    print(i, end=" ")
    i = i+1

print("\n-------------------")

# while
i = 0
while i < 5: # 0 to 4
    print(i)
    i = i+1

print("\n-------------------")

i = 0
while i <= 5: # 0 to 5
    print(i)
    i = i+1

